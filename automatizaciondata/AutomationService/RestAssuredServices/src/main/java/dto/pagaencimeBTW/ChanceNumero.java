package dto.pagaencimeBTW;

public class ChanceNumero {
    public String numero;
    public int superPlenoCombinado;
    public int superPlenoDirecto;
    public int plenoCombinado;
    public int plenoDirecto;
    public int pata;
    public int una;

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public int getSuperPlenoCombinado() {
        return superPlenoCombinado;
    }

    public void setSuperPlenoCombinado(int superPlenoCombinado) {
        this.superPlenoCombinado = superPlenoCombinado;
    }

    public int getSuperPlenoDirecto() {
        return superPlenoDirecto;
    }

    public void setSuperPlenoDirecto(int superPlenoDirecto) {
        this.superPlenoDirecto = superPlenoDirecto;
    }

    public int getPlenoCombinado() {
        return plenoCombinado;
    }

    public void setPlenoCombinado(int plenoCombinado) {
        this.plenoCombinado = plenoCombinado;
    }

    public int getPlenoDirecto() {
        return plenoDirecto;
    }

    public void setPlenoDirecto(int plenoDirecto) {
        this.plenoDirecto = plenoDirecto;
    }

    public int getPata() {
        return pata;
    }

    public void setPata(int pata) {
        this.pata = pata;
    }

    public int getUna() {
        return una;
    }

    public void setUna(int una) {
        this.una = una;
    }
}
