package dto.consultparameters;

public class Loteria {
    public String codLoteria;
    public String nombreCorto;
    public String fecha;
    public String hora;
    public String loteriaDia;
    public int cifrasNumero;
    public int cifrasSerie;
    public String nombreCompletoLoteria;

    public String getCodLoteria() {
        return codLoteria;
    }

    public void setCodLoteria(String codLoteria) {
        this.codLoteria = codLoteria;
    }

    public String getNombreCorto() {
        return nombreCorto;
    }

    public void setNombreCorto(String nombreCorto) {
        this.nombreCorto = nombreCorto;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getLoteriaDia() {
        return loteriaDia;
    }

    public void setLoteriaDia(String loteriaDia) {
        this.loteriaDia = loteriaDia;
    }

    public int getCifrasNumero() {
        return cifrasNumero;
    }

    public void setCifrasNumero(int cifrasNumero) {
        this.cifrasNumero = cifrasNumero;
    }

    public int getCifrasSerie() {
        return cifrasSerie;
    }

    public void setCifrasSerie(int cifrasSerie) {
        this.cifrasSerie = cifrasSerie;
    }

    public String getNombreCompletoLoteria() {
        return nombreCompletoLoteria;
    }

    public void setNombreCompletoLoteria(String nombreCompletoLoteria) {
        this.nombreCompletoLoteria = nombreCompletoLoteria;
    }
}
