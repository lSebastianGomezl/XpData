package dto.maxichancePVMA;

public class ChanceNumero {
    public String numero;
    public int plenoDirecto;
    public int superPlenoDirecto;

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public int getPlenoDirecto() {
        return plenoDirecto;
    }

    public void setPlenoDirecto(int plenoDirecto) {
        this.plenoDirecto = plenoDirecto;
    }

    public int getSuperPlenoDirecto() {
        return superPlenoDirecto;
    }

    public void setSuperPlenoDirecto(int superPlenoDirecto) {
        this.superPlenoDirecto = superPlenoDirecto;
    }
}
